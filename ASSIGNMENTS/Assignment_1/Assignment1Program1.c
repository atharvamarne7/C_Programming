//////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :     Divide
//  Description :       Division of two numbers  
//  Input :             Integer
//  Output :            Integer       
//  Author :            Atharva Marne
//  Date :              16/10/2025
//
