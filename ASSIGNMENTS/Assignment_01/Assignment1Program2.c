#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :     Display
//  Description :       Display 5 times Marvellous 
//  Input :             -
//  Output :            5 times Marvellous       
//  Author :            Atharva Marne
//  Date :              16/10/2025
//
//////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;                          // Counter for loop initialized with 0             
    
    for(iCnt =1; iCnt <= 5; iCnt++)
    {
        
        printf("Marvellous \n");

    }

}            

//////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function of the application
//
//////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
    
    return 0;
    
}       

